package lec1;

public class Lecture3 {

    public static void main(String[] args) {

        //실수자료형
        //float
        float fpi = 3.141592f;
        System.out.println("fpi = " + fpi);

        //double
        double dpi = 3.141592;
        System.out.println("dpi = " + dpi);

        //오차 발생
        float minus = .3f - .125f;
        System.out.println("minus = " + minus);

        double min = .3 - .125;
        System.out.println("min = " + min);
    }
}
